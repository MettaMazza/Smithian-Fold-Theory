"""Fold Impartial Games — the fold's NATIVE game theory.

Nim-values are xor: carry-free base-2 arithmetic. The fold is the shift on
base-2 digits. This is not analogy (as chess was) but identity — impartial
game theory lives in the fold's own arithmetic.

Phase 1 (this module): THEOREM-FORCED CALIBRATION. For k heaps of b bits
under interleaved packing, the losing set (xor of heaps = the additive
identity) is an F2-LINEAR SUBSPACE of the index space; therefore its
spectrum is supported EXACTLY on the dual subspace — the masks that pair
equal-significance bits across heaps — with all magnitudes equal. This is
predicted before any computation, like the bit-reversal self-test: the
instrument must reproduce it exactly or the instrument is broken.

(The author's original Nim observation — P-position spacings 5 = 2^2+1 and
257 = 2^8+1 — is the 1-dimensional shadow of this law.)
"""

